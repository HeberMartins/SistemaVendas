    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Cliente c = new Cliente();
        c.setId(this.idCliente);
        c.setNome(txbNome.getText());
        c.setEndereco(txbEndereco.getText());
        c.setEmail(txbEmail.getText());

        ClienteDAO cDAO = new ClienteDAO();
        cDAO.editar(c);

        JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!");
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Realmente Excluir?");

        if (resposta == JOptionPane.YES_OPTION) {
            ClienteDAO cDAO = new ClienteDAO();
            cDAO.excluir(this.idCliente);

            JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
            this.dispose();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


