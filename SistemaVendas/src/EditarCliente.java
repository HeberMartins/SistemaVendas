    private void carregarCliente() {
        ClienteDAO cDAO = new ClienteDAO();

        Cliente c = cDAO.getCliente(this.idCliente);

        if (c != null) {
            txbNome.setText(c.getNome());
            txbEndereco.setText(c.getEndereco());
            txbEmail.setText(c.getEmail());
        }
    }

